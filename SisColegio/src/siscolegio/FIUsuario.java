    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        try {
            if (FIListaUsuarios.modif) {
                tbxempleado.setText(ADNEmpleado.ObtenerNombreEmpleado(idemp));
                tbxnick.setText(nick);
                pfdclave1.setText(clave);
                cbxtipo.setSelectedItem(tipo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }//GEN-LAST:event_formInternalFrameOpened

    private void btnbuscarempleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarempleadoActionPerformed
        CDBuscarEmpleado c = new CDBuscarEmpleado(null, closable);
        c.setVisible(true);
        if (idemp > 0) {
            tbxempleado.setText(nomemp);
        }
    }//GEN-LAST:event_btnbuscarempleadoActionPerformed
