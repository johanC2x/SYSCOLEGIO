    public static int idusu = 0, idemp = 0;
    public static String nick, clave, tipo, nomemp;
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (!String.valueOf(pfdclave1.getPassword()).equals(String.valueOf(pfdclave2.getPassword()))) {
                JOptionPane.showMessageDialog(rootPane, "Las claves no coinciden");
            } else {
                String nic = tbxnick.getText().trim();
                String cla = String.valueOf(pfdclave1.getPassword());
                String tip = cbxtipo.getSelectedItem().toString();
                boolean gua = ADNUsuario.Guardar(new Usuario(idusu, idemp, nic, cla, tip));
                if (gua) {
                    if (FIListaUsuarios.modif) {
                        JOptionPane.showMessageDialog(rootPane, "Datos actualizados correctamente");
                        FIListaUsuarios.modif = false;
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Datos guardados correctamente");
                    }
                    setVisible(false);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

